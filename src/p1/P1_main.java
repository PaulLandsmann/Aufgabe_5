package p1;

public class P1_main {

	public static void main(String[] args) {
		
		int x = 0;
		int y = 0;
		
		while (x <= 10) {
			x = x + 1;
			System.out.println(x);
		}
		
		for(int i = 1; i <= 11; i++) {
			System.out.println(i);
		}
		
		
		do {
			y++;
			System.out.println(y);
		} while(y <= 10);
			

	}

}
